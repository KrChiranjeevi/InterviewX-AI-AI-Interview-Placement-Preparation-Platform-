export const getCompanyConfig = (companyId) => {
  return COMPANY_CONFIGS[companyId?.toLowerCase()] || null;
};

export const getTrackConfig = (companyId, trackId) => {
  const company = getCompanyConfig(companyId);
  if (!company) return null;
  return company.tracks.find(t => t.id === trackId) || null;
};

export const getAllCompanies = () => Object.values(COMPANY_CONFIGS);

export const getServiceCompanies = () =>
  Object.values(COMPANY_CONFIGS).filter(c => c.type === 'service');

export const getProductCompanies = () =>
  Object.values(COMPANY_CONFIGS).filter(c => c.type === 'product');

